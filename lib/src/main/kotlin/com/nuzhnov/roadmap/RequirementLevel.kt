package com.nuzhnov.roadmap

/**
 * Требуемый уровень знания технологии:
 * * [REQUIRED] - знание обязательно;
 * * [DESIRABLE] - знание рекомендуется для освоения;
 * * [OPTIONAL] - дополнительное знание для освоения;
 * * [NOT_RECOMMENDED] - знание, которое не рекомендуется к изучению;
 */
enum class RequirementLevel {
    REQUIRED,
    DESIRABLE,
    OPTIONAL,
    NOT_RECOMMENDED
}
